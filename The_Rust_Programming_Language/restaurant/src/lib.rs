mod back_of_house {
    pub struct Breakfast {         // Na struct pública, as propriedades
        pub toast: String,        // ainda são privadas. Tornar uma
        seasonal_fruit: String,  // propriedade publica, não deixa o
    }                           // resto público

    impl Breakfast {
        pub fn summer(toast: &str) -> Breakfast {
            Breakfast {
                toast: String::from(toast),
                seasonal_fruit: String::from("peaches"),
            }
        }
    }

    pub enum Appetizer {  // No enum público,
        Soup,            // tudo é público.
        Salad,
    }

}

